func (r *resourceWorkspaceGitOutboundPolicy) Delete(ctx context.Context, req resource.DeleteRequest, resp *resource.DeleteResponse) {
	tflog.Debug(ctx, "DELETE", map[string]any{
		"action": "start",
	})

	var state resourceWorkspaceGitOutboundPolicyModel

	if resp.Diagnostics.Append(req.State.Get(ctx, &state)...); resp.Diagnostics.HasError() {
		return
	}

	timeout, diags := state.Timeouts.Delete(ctx, r.pConfigData.Timeout)
	if resp.Diagnostics.Append(diags...); resp.Diagnostics.HasError() {
		return
	}

	ctx, cancel := context.WithTimeout(ctx, timeout)
	defer cancel()

	// Reset to defaults by sending an empty request body.
	resp.Diagnostics.AddWarning(
		"Resetting Git Outbound Policy to default",
		fmt.Sprintf(
			"Resource %s does not support deletion. The Git Outbound Policy will be reset to its default value.",
			r.TypeInfo.Name,
		),
	)

	reqDelete := fabcore.NetworkRules{}

	_, err := r.client.SetGitOutboundPolicy(ctx, state.WorkspaceID.ValueString(), reqDelete, nil)
	if resp.Diagnostics.Append(utils.GetDiagsFromError(ctx, err, utils.OperationDelete, nil)...); resp.Diagnostics.HasError() {
		return
	}

	tflog.Debug(ctx, "DELETE", map[string]any{
		"action": "end",
	})
}

func (r *resourceWorkspaceGitOutboundPolicy) get(ctx context.Context, model *resourceWorkspaceGitOutboundPolicyModel) diag.Diagnostics {
	tflog.Trace(ctx, fmt.Sprintf("getting %s for Workspace ID: %s", r.TypeInfo.Name, model.WorkspaceID.ValueString()))

	respGet, err := r.client.GetGitOutboundPolicy(ctx, model.WorkspaceID.ValueString(), nil)
	if diags := utils.GetDiagsFromError(ctx, err, utils.OperationRead, fabcore.ErrCommon.EntityNotFound); diags.HasError() {
		return diags
	}

	model.set(model.WorkspaceID.ValueString(), respGet.NetworkRules)

	return nil
}
